// ARRAY METHODS

// Push Method
let arr1 = ["hello", "world", "welcome", "java"];
arr1.push("script", "frontend");
console.log(arr1,"push method");

// Pop Method
let arr2 = ["hello", "world", "welcome", "java"];
arr2.pop();
console.log(arr2,"pop method");

// UnShift Method
let arr3 = ["hello", "world", "welcome", "java"];
arr3.unshift(1,2,3,4);
console.log(arr3,"unshift method");

// Shift Method
let arr4 = ["hello", "world", "welcome", "java"];
arr4.shift();
console.log(arr4,"shift method");

// Join Method
let arr5 = ["java", "is", "programming", "language"];
let sentence = arr5.join(" ");
console.log(sentence,"join method");












